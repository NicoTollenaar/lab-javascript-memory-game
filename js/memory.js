class MemoryGame {
  constructor(cards) {
    this.cards = cards;
    this.pickedCards = [];
    this.pairsClicked = 0;
    this.pairsGuessed = 0;
  }

  shuffleCards() {
    if (!this.cards) {
      return undefined;
    }
    let shuffledCards = [];
    let deepCopy = JSON.parse(JSON.stringify(this.cards));
    let randomIndex = 0;
    while (deepCopy.length > 0) {
      randomIndex = Math.floor(Math.random()*deepCopy.length);
      shuffledCards.push(deepCopy[randomIndex]);
      deepCopy.splice(randomIndex, 1);
    }
    this.cards = shuffledCards;
  }

  checkIfPair(card1, card2) {
    this.pairsClicked++;
    if (card1 === card2) {
      this.pairsGuessed++;
      return true;
    } else {
      return false;
    }
  }

  checkIfFinished() {
    let answer = this.pairsGuessed === (this.cards.length / 2);
    if (answer) {
      return true;
    } else {
      return false;
    }
  }
}

